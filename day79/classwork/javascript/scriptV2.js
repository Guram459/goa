function Big(x) {
    x.style.height = '200px';
    x.style.width = '200px';
    x.style.backgroundColor = "blue"
}

function Normal(x) {
    x.style.backgroundColor = "green"
    x.style.height = '100px'
    x.style.width = '100px'
    x.innerHtml = 'Normal'
}

